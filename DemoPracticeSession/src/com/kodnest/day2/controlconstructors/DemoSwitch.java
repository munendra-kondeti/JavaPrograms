package com.kodnest.day2.controlconstructors;

public class DemoSwitch {

	public static void main(String[] args) {
		int num = 2;
		// switch
		switch (num) {
		case 1:
			System.out.println("1");
			break;
		case 2:
			System.out.println("2");
			break;
		default:
			System.out.println("Its default");

		}

	}

}
